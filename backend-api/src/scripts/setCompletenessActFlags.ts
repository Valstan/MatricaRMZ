async function loadBrandIdByName(): Promise<Map<string, string>> {
  const typeRows = await db
    .select({ id: entityTypes.id })
    .from(entityTypes)
    .where(and(eq(entityTypes.code, 'engine_brand'), isNull(entityTypes.deletedAt)))
    .limit(1);
  const typeId = typeRows[0]?.id ? String(typeRows[0].id) : '';
  if (!typeId) throw new Error('entity_type engine_brand не найден');
  const defRows = await db
    .select({ id: attributeDefs.id })
    .from(attributeDefs)
    .where(and(eq(attributeDefs.entityTypeId, typeId), eq(attributeDefs.code, 'name'), isNull(attributeDefs.deletedAt)))
    .limit(1);
  const defId = defRows[0]?.id ? String(defRows[0].id) : '';
  if (!defId) throw new Error('attribute_def name у engine_brand не найден');
  const rows = await db
    .select({ entityId: attributeValues.entityId, v: attributeValues.valueJson })
    .from(attributeValues)
    .innerJoin(entities, eq(entities.id, attributeValues.entityId))
    .where(and(eq(attributeValues.attributeDefId, defId), isNull(attributeValues.deletedAt), isNull(entities.deletedAt)));
  const out = new Map<string, string>();
  for (const r of rows) {
    let name = '';
    try {
      name = String(JSON.parse(String(r.v ?? '""')) ?? '');
    } catch {
      name = String(r.v ?? '');
    }
    const key = compact(name);
    if (key && !out.has(key)) out.set(key, String(r.entityId));
  }
  return out;
}

async function main() {
  console.log(APPLY ? '!!! РЕЖИМ ЗАПИСИ (--apply) !!!' : '--- DRY-RUN (без записей; --apply для выполнения) ---');

  const brandIdByKey = await loadBrandIdByName();
  const listed = await listWarehouseNomenclaturePartSpecs();
  if (!listed.ok) throw new Error(`Не удалось загрузить детали: ${listed.error}`);
  const parts: PartRow[] = listed.rows.map((r) => ({
    id: r.id,
    name: r.name,
    code: r.code,
    isActive: r.isActive,
    brandLinks: r.brandLinks,
  }));
  console.log(`деталей в справочнике: ${parts.length}\n`);

  // partId -> актуальный массив линков (мутируем в памяти, пишем один раз в конце)
  const linksByPart = new Map<string, PartSpecBrandLink[]>();
  for (const p of parts) linksByPart.set(p.id, p.brandLinks.map((l) => ({ ...l })));
  const dirtyParts = new Set<string>();
  const stats = { flagAlready: 0, flagSet: 0, linkAdded: 0, partCreated: 0 };
  const createdPlan: Array<{ brandId: string; name: string; code: string }> = [];

  for (const [brandName, items] of DATA) {
    const brandId = brandIdByKey.get(compact(brandName));
    console.log(`\n=== ${brandName} ${brandId ? `(${brandId})` : '— МАРКА НЕ НАЙДЕНА, пропуск'} ===`);
    if (!brandId) continue;
    for (const [name, code] of items) {
      const found = findPart(parts, { name, code }, brandId);
      if (!found) {
        console.log(`   + СОЗДАТЬ: «${name}» ${code} → новая деталь + линк (акт компл.)`);
        createdPlan.push({ brandId, name, code: asmFromCode(code) });
        stats.partCreated += 1;
        continue;
      }
      const { part, how } = found;
      const links = linksByPart.get(part.id)!;
      const link = links.find((l) => String(l.engineBrandId) === brandId);
      if (!link) {
        links.push({ id: randomUUID(), engineBrandId: brandId, assemblyUnitNumber: asmFromCode(code), quantity: 1, inCompletenessAct: true });
        dirtyParts.add(part.id);
        stats.linkAdded += 1;
        console.log(`   + ЛИНК: «${name}» ${code} → ${part.name} [${part.code ?? '—'}] (${how}) — новый линк + галочка`);
      } else if (link.inCompletenessAct === true) {
        stats.flagAlready += 1;
        console.log(`   = OK: «${name}» ${code} → ${part.name} [${part.code ?? '—'}] (${how}) — галочка уже стоит`);
      } else {
        link.inCompletenessAct = true;
        if (isEmptyAsm(link.assemblyUnitNumber)) link.assemblyUnitNumber = asmFromCode(code);
        dirtyParts.add(part.id);
        stats.flagSet += 1;
        console.log(`   ✓ ГАЛОЧКА: «${name}» ${code} → ${part.name} [${part.code ?? '—'}] (${how})`);
      }
    }
  }

  console.log(`\n=== ИТОГО (план) ===`);
  console.log(JSON.stringify({ ...stats, dirtyParts: dirtyParts.size }, null, 2));

  if (!APPLY) {
    console.log('\n(dry-run — записей не было. Повторите с --apply после ревью.)');
    return;
  }

  console.log('\n=== ЗАПИСЬ ===');
  for (const { brandId, name, code } of createdPlan) {
    let partId = '';
    const created = await createDirectoryPart({ name, code });
    if (created.ok) partId = created.part.id;
    else {
      const dup = String(created.error || '').match(/duplicate part exists:\s*([0-9a-f-]{36})/i);
      if (dup?.[1]) partId = dup[1];
      else {
        console.log(`   ✖ createDirectoryPart «${name}» ${code}: ${created.error}`);
        continue;
      }
    }
    const cur = await getWarehouseNomenclaturePartSpec({ nomenclatureId: partId });
    const base = cur.ok && cur.spec ? cur.spec : { code: null, dimensions: [], brandLinks: [] };
    const brandLinks = base.brandLinks.map((l) => ({ ...l }));
    const link = brandLinks.find((l) => String(l.engineBrandId) === brandId);
    if (link) link.inCompletenessAct = true;
    else brandLinks.push({ id: randomUUID(), engineBrandId: brandId, assemblyUnitNumber: code, quantity: 1, inCompletenessAct: true });
    const up = await upsertWarehouseNomenclaturePartSpec({ nomenclatureId: partId, spec: { ...base, brandLinks } });
    if (!up.ok) console.log(`   ✖ spec «${name}»: ${up.error}`);
    else console.log(`   + создана «${name}» ${code} (${partId})`);
  }
  let written = 0;
  for (const partId of dirtyParts) {
    const cur = await getWarehouseNomenclaturePartSpec({ nomenclatureId: partId });
    if (!cur.ok) {
      console.log(`   ✖ get spec ${partId}: ${cur.error}`);
      continue;
    }
    const base = cur.spec ?? { code: null, dimensions: [], brandLinks: [] };
    const up = await upsertWarehouseNomenclaturePartSpec({
      nomenclatureId: partId,
      spec: { ...base, brandLinks: linksByPart.get(partId)! },
    });
    if (!up.ok) console.log(`   ✖ upsert ${partId}: ${up.error}`);
    else written += 1;
  }
  console.log(`\n=== ИТОГО (applied) === обновлено спецификаций: ${written}, создано деталей: ${createdPlan.length}`);
}

