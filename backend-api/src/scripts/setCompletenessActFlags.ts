// ─── Нормализация / матчинг ────────────────────────────────────────────────────
const clean = (v: unknown) => String(v ?? '').replaceAll(' ', ' ').replaceAll(/\s+/g, ' ').trim();
const compact = (v: string) => clean(v).toLowerCase().replaceAll('ё', 'е').replaceAll(/[^0-9a-zа-я]+/g, '');
/** Компактный ключ артикула: «сб» выкидывается целиком («Сб. 3301-15-30» → «33011530»). */
export function codeKey(v: string): string {
  return compact(v).replaceAll('сб', '');
}
/**
 * Ключи артикула с вариантами:
 *  - последний пробельный токен («НК-10М сб. 327-00-62» → и «3270062»);
 *  - составной «A/B» разворачивается («419-05-7/сб.419-05-12» → оба;
 *    «3320-372-4/11» → «3320-372-4» и «3320-372-11» — короткий хвост заменяет
 *    сегмент после последнего дефиса).
 */
export function codeKeys(v: string): string[] {
  const out = new Set<string>();
  const raw = clean(v);
  const addVariants = (s: string) => {
    const segs = s.split('/').map((x) => clean(x)).filter(Boolean);
    const first = segs[0] ?? '';
    if (first) out.add(codeKey(first));
    for (const seg of segs.slice(1)) {
      const k = codeKey(seg);
      if (k.length >= 5) out.add(k);
      else if (first.includes('-')) out.add(codeKey(first.slice(0, first.lastIndexOf('-') + 1) + seg));
      else if (k) out.add(k);
    }
  };
  addVariants(raw);
  const lastToken = raw.split(/\s+/).at(-1) ?? '';
  if (lastToken && lastToken !== raw) addVariants(lastToken);
  out.delete('');
  return [...out];
}
const nameKey = (v: string) => compact(v);
const nameTokens = (v: string) =>
  clean(v).toLowerCase().replaceAll('ё', 'е').split(/[^0-9a-zа-я]+/).filter(Boolean).sort().join('|');

function levenshtein(a: string, b: string): number {
  if (Math.abs(a.length - b.length) > 2) return 99;
  const prev = new Array(b.length + 1).fill(0).map((_, i) => i);
  for (let i = 1; i <= a.length; i += 1) {
    let diag = prev[0]!;
    prev[0] = i;
    for (let j = 1; j <= b.length; j += 1) {
      const tmp = prev[j]!;
      prev[j] = Math.min(prev[j]! + 1, prev[j - 1]! + 1, diag + (a[i - 1] === b[j - 1] ? 0 : 1));
      diag = tmp;
    }
  }
  return prev[b.length]!;
}

/** 3=точно, 2=перестановка слов/опечатка, 1=legacy-имя с вшитой маркой, 0=не похоже. */
export function nameSimilarity(target: string, candidate: string): number {
  const t = nameKey(target);
  const c = nameKey(candidate);
  if (!t || !c) return 0;
  if (t === c) return 3;
  if (nameTokens(target) === nameTokens(candidate)) return 2;
  if (t.length > 8 && levenshtein(t, c) <= 2) return 2;
  if (c.startsWith(t) && c.length - t.length <= 24) return 1; // «… В-59 УМС (сб. …)»
  // ≥2 общих слова («Коллектор выпускной левый» ⇄ «Трубопровод выпускной левый») —
  // допустимо только вместе с точным совпадением артикула (ветка 1 матчера).
  const ts = new Set(nameTokens(target).split('|'));
  const shared = nameTokens(candidate).split('|').filter((w) => ts.has(w)).length;
  if (shared >= 2) return 1;
  return 0;
}

const isMeaninglessCode = (code: string | null | undefined) => {
  const k = compact(String(code ?? ''));
  return !k || k.startsWith('det');
};
const EMPTY_ASM = new Set(['', 'незадано', 'неуказан', 'неуказано']);
const isEmptyAsm = (v: string | null | undefined) => {
  const k = compact(String(v ?? '').replaceAll('—', '').replaceAll('-', ''));
  return EMPTY_ASM.has(k);
};
/** «№ узла сборки» для записи: без префикса «Сб.», как в существующих артикулах. */
const asmFromCode = (raw: string) => clean(raw).replace(/^сб\.?\s*/i, '');

type PartRow = { id: string; name: string; code: string | null; isActive: boolean; brandLinks: PartSpecBrandLink[] };

export function findPart(parts: PartRow[], target: { name: string; code: string }, brandId: string): { part: PartRow; how: string } | null {
  const targetKeys = codeKeys(target.code);
  const codeMatches = parts.filter((p) => {
    if (isMeaninglessCode(p.code)) return false;
    const pKeys = codeKeys(String(p.code));
    return targetKeys.some((tk) => pKeys.some((pk) => pk === tk));
  });
  const pick = (list: PartRow[], how: string) => {
    if (!list.length) return null;
    const linked = list.filter((p) => p.brandLinks.some((l) => String(l.engineBrandId) === brandId));
    const active = (arr: PartRow[]) => arr.filter((p) => p.isActive);
    const pool = linked.length ? linked : list;
    const best = (active(pool).length ? active(pool) : pool)[0]!;
    return { part: best, how };
  };
  // 1) артикул + имя
  for (const minSim of [3, 2, 1]) {
    const hit = pick(codeMatches.filter((p) => nameSimilarity(target.name, p.name) >= minSim), `code+name(${minSim})`);
    if (hit) return hit;
  }
  // 2) имя среди деталей без осмысленного артикула
  const noCode = parts.filter((p) => isMeaninglessCode(p.code));
  for (const minSim of [3, 2]) {
    const cands = noCode.filter((p) => nameSimilarity(target.name, p.name) >= minSim);
    if (cands.length === 1 || (cands.length > 1 && minSim === 3)) {
      const hit = pick(cands, `name(${minSim})`);
      if (hit) return hit;
    }
  }
  return null;
}

