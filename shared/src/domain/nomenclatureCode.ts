/**
 * Синтетические коды-заглушки номенклатуры — общий распознаватель для клиента,
 * сервера и maintenance-скриптов.
 *
 * Клиент когда-то штамповал такой код вместо артикула при быстром создании
 * карточки, поэтому в проде накопились «артикулы, которых нет»: они маскируют
 * дубли (идентичность детали — пара имя+артикул) и не находятся поиском по
 * настоящему артикулу. Ф2 де-дупликации их вычищает, стоп-кран не даёт набежать
 * снова, поэтому маска обязана быть ОДНА на все три стороны.
 *
 * Генерируемых форм две, обе живут в прод-данных:
 *  • текущая — PREFIX + 8 цифр времени + 3 случайные (генератор удалён вместе
 *    со стоп-краном);
 *  • легаси — PREFIX + 8 hex-символов (генератора в коде нет давно).
 * Маска, знавшая только одну форму, дала тихий частичный прогон на 4 строках
 * из 145 с рапортом об успехе — GOTCHAS M41.
 *
 * Префиксы — ровно те, что штамповало само приложение (пресеты справочников
 * `nomenclatureDirectoryPresets.ts` + легаси `NM` зеркала номенклатуры).
 * Живые вендорские коды под форму не подходят и остаются нетронутыми:
 * `SRV-001` (3 цифры), `NM-1050` (4 цифры), `3301-15-30` (не начинается с букв).
 */
export const SYNTHETIC_NOMENCLATURE_CODE_PREFIXES = [
  'DET',
  'NM',
  'TLS',
  'PRD',
  'SRV',
  'ASM',
  'ENG',
  'CMP',
  'MAT',
  'CNS',
] as const;

const SYNTHETIC_NOMENCLATURE_CODE_RE = new RegExp(
  `^(${SYNTHETIC_NOMENCLATURE_CODE_PREFIXES.join('|')})-(\\d{11}|[0-9A-F]{8})$`,
);

/** Код сгенерирован приложением как заглушка (а не введён человеком как артикул). */
export function isSyntheticNomenclatureCode(code: string | null | undefined): boolean {
  return SYNTHETIC_NOMENCLATURE_CODE_RE.test(String(code ?? '').trim().toUpperCase());
}

